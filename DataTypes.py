
print("Welcome to python programming")
# Integer
x = 10
print("Integer example:", x)
print("Type:", type(x))

# Float
y = 3.14
print("\nFloat example:", y)
print("Type:", type(y))

# String
name = "Dorothy Martha"
print("\nString example:", name)
print("Type:", type(name))

# Boolean
is_valid = True
print("\nBoolean example:", is_valid)
print("Type:", type(is_valid))

# List
numbers = [1, 2, 3, 4, 5]
print("\nList example:", numbers)
print("Type:", type(numbers))

# Tuple
coordinates = (10, 20)
print("\nTuple example:", coordinates)
print("Type:", type(coordinates))

# Dictionary
student = {"name": "Alice", "age": 18, "grade": "A"}
print("\nDictionary example:", student)
print("Type:", type(student))
